            <p class="mbr-text align-left pb-3 mbr-fonts-style display-5">Ocruxaves fue una editorial de San Fernando que estuvo en funcionamiento mientras mi Papá pasó por esta tierra, prestando servicio a la comunidad de nuevos autores que deseaban publicar sus libros, como así también a eminencias literarias de la zona, tales como Carlos Enrique Urquía, Francisco Vázquez y Héctor Prado, entre otros.
<br>
<br>Comenzó siendo una revista literaria, allá por mediados de los años ´80, para luego convertirse en editorial, que publicó más de 300 ediciones de autor hasta su deceso, el 28 de enero de 2012. Se destacaban principalmente las ediciones de cuentos y poesía como así también novelas y, en menor medida, ensayos, trabajos de investigación y biografías, entre otras publicaciones.
<br>
<br>Paralelamente a este trabajo, su pasión, Hugo Enrique Boulocq era también escritor. Publicó en vida tres libros de cuentos: Enroque en la Ventana, En la Prisión de los Bárbaros y Siempre Llueven Flores en Manantiales. Los mismos pueden descargarse gratuitamente de este sitio en formato PDF, en la sección de libros. Existe también para bajar un Breviario de Ensayos, que lamentablemente no vio la luz mientras vivió, el cual se encontraba archivado en su computadora y que creímos oportuno con mi familia editar digitalmente. 
<br>Una biografía más precisa puede leerse en la sección correspondiente junto un detalle puntual de publicaciones, más allá de los libros de cuentos mencionados.
<br>
<br>Este sitio web, que en un principio fue la cara en internet de Ediciones Ocruxaves, con información de libros publicados, datos de interés para nuevos autores y demás detalles del mundo editorial, intenta a partir de ahora rendirle homenaje, quizá en este comienzo de manera un poco desprolija, reuniendo su labor literaria y participaciones públicas, sumando perdiódicamente cuanto material podamos encontrar.
<br>
<br>Toda información relacionada con él será bien recibida para generar nuevas secciones dentro del sitio, como pueden ser anécdotas, reportajes y otros datos de interés.</p>
